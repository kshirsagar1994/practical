package Array;
//example of single dimensional array
public class Single_Dimension_ArrayEg2 {
	public static void main(String args[]) {
		//decalration, initialization & installation
		String [] names= {"Aishwarya","Ajay","susmit","vishal","pruthvi","Vidya"};
		for(int i=0;i<names.length;i++) {
			System.out.println(names[i]);
		}
		System.out.println();
		int [] phone= {123,456,789,987,654,321};
		for(int j=0;j<phone.length;j++) {
			System.out.println(phone[j]);
		}
}
}
